
<p>Norske S&oslash;k er et prosjekt som startet ca. 21.06.2011. Dets m&aring;l er &aring; f&aring; s&aring; 
mange s&oslash;keverkt&oslash;y/konverteringsverkt&oslash;y p&aring; en plass (v&aelig;re en portal) for nordmenn. 
Dette fordi det alltids er noe info man trenger kjapt, uten &aring; å møte en vegg med reklame. 
</p>
<p>Innholdet på sidene har endret seg noe. Og det kan hende at det kommer til å endre seg enna mere. Målet med sidene
	har også endret seg siden jeg startet med det. Et av målene som jeg har fokusert på den siste tiden er søke-resultatet
	på sted/postnummer. Informasjons-siden man kommer frem til skal inneholde så mye nytte-full info som mulig. Som jeg mener 
	er nyttefullt iallefall. Målet er iallefall at flest mulig skal kunne bruke siden som start-side. Finne nyheter fra sitt 
	distrikt fort og kunne konfigurere siden mest mulig selv. 
</p>
<p>Apropos hva det er som jobbes med nå: Sikker innlogging(ferdig) og bruker-grensesnitt(hva skal være lov og ikke lov).
	Ideer som har dukket opp ang. den andre delen er at brukeren skal kunne konfigurere noen av menyene samt innhold av
	søke-side.
	Den andre delen kan ta lit tid, men det kan hende det dukker opp noen prøve-versjoner om kort tid, med meget begrensede
	muligheter vel og merke. Men noe må man begynne med.
</p>

